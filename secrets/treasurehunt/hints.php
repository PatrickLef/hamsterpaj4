	<h1><strong style="color: black;">2009-06-03 Skattjakten är över!</strong></h1>
	<img src="http://images.hamsterpaj.net/images/users/full/910780.jpg" style="float: left; margin: 10px;" />
	<p>
		Onsdag 3e juni hittade <a href="/traffa/profile.php?id=910780">Dead_Alone</a> skatten, som var gömd
		vid Zinkensdamms idrottsplats i Stockholm. Här kan du läsa om hur ledtrådarna var tänkta:
	</p>
	
	<h2>Ledtråd #1 - Lena PH</h2>
	<p>
		Genom att pussla om bilden fick man fram en bild på Lena Philipsson där hon sjunger i en mikrofon och klämmer
		på sin ena tutte. Bilden var tänkt som en rebus av "Mikrofonkåt".<br />
		Bildfilen hette "0m23s.jpg", den som lyssnar på <a href="http://open.spotify.com/track/2W52nDjzhIrnlQK8OJaFRs">Petter - mikrofonkåt</a>
		hör vid 00:23 texten <em>"Från Slussen - Hornstull - Zinkensdamm - Vietnam"</em>.
	</p>

	<h2>Ledtråd #2 - Tutanego</h2>
	<p>
		Bilden föreställde en fejkad nyhet på Nyheter24.se. Två tackor figurerar på bilden och 6056 uppges vara kvar längst ner
		i fartyget. Ostindiefararen Götheborg gick på grund i Göteborgs hamninlopp, som barlast fanns just 6056 tackor tutanego kvar.
		En snabb koll på Wikipedia säger att tutanego är en legering med högt zinkinnehåll.
	</p>
	
	<h2>Ledtråd #3 - Agent Orange och IP</h2>
	<p>
		Den som kopplar att orangefärgade James Bond kan tolkas som "agentorange" och fyller i detta i rutan nedanför får en länk till
		Awapatents förstasida.<br />
		Awapatent sysslar med upphovsrätt - Intellectual Property, IP. IP nämns överallt på Awapatents förstasida och det hör
		förstås ihop med Zinkensdamms IP.<br />
		Bilden heter "fourtyonepointthree", 41.3. Baklänges blir det 3.14, dvs PI, som rättvänt blir IP.
	</p>

	<h2>Ledtråd #4 - Vietnam</h2>
	<p>
		Helikoptern som sprutar ut bekämpningsmedel flyger över Vietnamn. Vietnamn hänger ihop med Zinkensdamm i Petters låt Mikrofonkåt.
	</p>

	<h2>Ledtråd #5 - Mikrofonkåt</h2>
	<p>
		Ännu en referens till Petters låt Mikrofonkåt.
		Första delen "Oavsett vad som händer har jag totalkoll" syftar på raden <em>"vad som än händer -total koll"</em> i tredje versen.<br />
	"titta mellan Raoul Wallenberg och Svenska hackers" är en referens till
	<a href="http://www.sr.se/sida/artikel.aspx?programid=2519&artikel=1412499">listan över dokumentärer som P3 gjort.</a>
		Mellan "Svenska hackers" och "Raoul Wallenberg" återfinns "Veganrörelsen" som i Petters låt nämns vid 00:30, "Flowar som ett gäng militanta veganer".
	</p>

	<h2>Ledtråd #6 - Brev från kolonien</h2>
	<p>
		Skrivbordet som syns på bilden visar när Johan arbetar med nya Digga. Under skärmarna ligger ett brev adresserat till 
		<strong>Morsan o stabben</strong> med avsändare <strong>Älsklingsgrabben</strong>.<br />
		Cornelis Vreeswijks sång "Brev från kolonien" inleds med texten:<br />
		<em>"Hejsan morsan, hejsan stabben, här e brev från älsklingsgrabben"</em>.<br />
		Ledtråden syftar på koloniområdet bakom Zinkensdamms IP i Stockholm.
	</p>

	<h2>Ledtråd #7 - 31 lotter</h2>
	<p>
		Fotot föreställer 31 lotter, precis som Zinkensdamms koloniförening där det finns 31 lotter.<br />
		Genom att byta ut bokstäverna i det kryptiska meddelandet till föregående bokstav i alfabetet får man fram följande meddelande:
		<pre>
			En halvmeter ovan mark
			bakom, under
			leta efter zsg
			backe med grönska 
		</pre>
		ZSG syftar på en klotter-tag, "ZSG", på en vägg bakom Zinkensdamms IP. Precis mot koloniområdet vid en grönskande backe.
	</p>
	
	<h2>Ledtråd #8 - Zinkensdamms IP</h2>
	<p>
		Fotot visar en kvinna som gör armhävningar på Zinkensdamms IP. Bilden finns precis vid ingången till idrottsplatsen.<br />
		Den som tar första bokstäven i varje ord ur nonsensmeningen "Många i Karlskrona ropar ofta fel, ovala naglar klöser åtminstone tillräckligt"
		får fram Mikrofonkåt.
	</p>
	
	<h2>Själva skatten</h2>
	<p>
		Skatten satt inkilad under väggen på en byggnad som avgränsar Zinkensdamms IP från koloniområdet bakom.
		Skatten var ett ihoptejpat papperspaket med koden "Kycklingburgare" på insidan.<br />
		Priset: två Hamsterpaj-T-shirts.
	</p>
	
